#include "calculator.h"

/**
 * @brief Implements the add function.
 */
int add(int a, int b) {
    return a + b;
}

int sub(int a, int b) {
    return a - b;
}

int mul(int a, int b) {
    return a * b;
}

int div(int a, int b) {
    return a / b;
}

int rem(int a, int b) {
    return a % b;
}

int square(int a) {
    return a * a;
}