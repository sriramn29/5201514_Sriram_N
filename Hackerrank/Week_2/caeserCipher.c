char* caesarCipher(char* s, int k) {
    // Calculate the effective shift to handle values of k > 26.
    // The modulo operator ensures the shift wraps around the alphabet.
    int effective_shift = k % 26;
    
    // Loop through each character of the string until the null terminator ('\0') is reached.
    for (int i = 0; s[i] != '\0'; i++) {
        // Check if the character is a lowercase letter.
        if (islower(s[i])) {
            // Apply the cipher formula for lowercase letters:
            // 1. s[i] - 'a': Convert the character to a 0-25 range.
            // 2. + effective_shift: Add the shift value.
            // 3. % 26: Use modulo to wrap around the alphabet.
            // 4. + 'a': Convert the shifted value back to a lowercase letter.
            s[i] = (s[i] - 'a' + effective_shift) % 26 + 'a';
        } 
        // Check if the character is an uppercase letter.
        else if (isupper(s[i])) {
            // Apply the same logic as above, but for uppercase letters.
            s[i] = (s[i] - 'A' + effective_shift) % 26 + 'A';
        }
        // If the character is not an alphabet, it remains unchanged.
    }
    // Return a pointer to the modified string.
    return s;
}

